package Org.abc;

public class Employe {
	
	private int id;
	private String name;
	private long phone;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public long getPhone() {
		return phone;
	}
	 void setPhone(long phone) {
		this.phone = phone;
	}
	
	

}
